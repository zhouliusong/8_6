# 客户端：简单的理解就是本地的计算机
# 服务端（服务器）：简单的理解就是远程计算机
# 客户端向服务器发起的是请求，服务器向客户端返回的是响应。

# http/https/SSL（必须知道对应的中文含义）
# http：超文本传输协议，它是互联网上应用最为广泛的一种网络协议，当我们在浏览器的地址中输入一个URL或者单击一个连接的时候，URL就确定了要浏览的地址是什么。浏览器通过HTTP/HTTPS，将服务器上的网页代码提取出来，并翻译成漂亮的网页。

# SSL：安全套接层，主要是用于web的安全传输协议，在传输层对网络连接进行加密，保障互联网（Internet）上的数据传输安全。

# https：相当于http的升级版，（http+ssl）

















