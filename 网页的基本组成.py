# 通常情况下一个网页可以分为三个部分：HTML，CSS，JavaScript。如果把网页比作一个人的话，HTML相当于人的骨架，CSS相当于皮肤，JavaScript相当于是肌肉，这三者结合才能形成一个完善的网页
# html是超文本标记语言。它不能算做一门编程语言，但是他是一种标记语言。HTML使用标记标签来描述网页。网页包括文字，图片，按钮，视频等各种元素，其最基本的架构就是HTML。
# html定义了网页的结构，但是html页面并不美观，为了让网页变得更加好看，所以产生了CSS。
# HTML和CSS虽然给用户提供了美观的页面，但是这样的页面缺少交互性。于是，便是用JavaScript来给页面添加一些交互和动画效果。如下载进度条，提示框，轮播图等等。JavaScript也是一门编程语言，和Python一样属于脚本语言的范畴。













