# 1、基本概念
'''
爬虫：模拟用户浏览网页的行为自动批量的获取网站数据的程序。
反爬虫：在服务器端使用一些技术来阻止爬虫程序的执行，对网站的数据进行保护。
'''

# 2、爬虫的目的
'''
1、获取数据，用于填充公司的数据库。
2、通过爬虫数据，制作搜索引擎。
3、通过爬虫数据，做数据采集和数据分析。
'''
# 3、反爬虫的目的
'''
1、针对一些初级爬虫，简单粗暴，不会考虑服务器的压力，会导致服务器瘫痪。
2、针对一些失控的爬虫，爬虫的数据量很庞大，忘记关闭爬虫程序的。
'''
